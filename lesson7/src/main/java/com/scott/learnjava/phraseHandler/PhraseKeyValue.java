package com.scott.learnjava.phraseHandler;

public class PhraseKeyValue {
    private String key;
    private String value;

    public PhraseKeyValue(String key, String value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public String getValue() {
        return value;
    }
}
